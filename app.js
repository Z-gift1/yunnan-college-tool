// 根据分数筛选学校
function filterSchools(category, score, region) {
    // 首先按类别筛选
    let filtered = schoolsData.filter(school => school.类别 === category);
    
    // 按地区筛选
    if (region === "云南昆明") {
        filtered = filtered.filter(school => school.所在地 === "云南昆明");
    } else if (region === "云南大理") {
        filtered = filtered.filter(school => school.所在地 === "云南大理");
    } else if (region === "云南其他") {
        filtered = filtered.filter(school => school.所在地 !== "云南昆明" && school.所在地 !== "云南大理");
    } else if (region === "省外") {
        // 如果用户选择省外，返回提示
        return { schools: [], isOutside: true };
    }
    
    // 按分数筛选（考虑一定范围）
    const scoreRange = 100; // 上下浮动100分
    filtered = filtered.filter(school => 
        school.最低分 >= (score - scoreRange) && school.最低分 <= (score + 50)
    );
    
    // 排序
    filtered.sort((a, b) => b.最低分 - a.最低分);
    
    // 分梯度
    const chong = []; // 冲：分数略高于考生
    const wen = [];   // 稳：分数接近考生
    const bao = [];   // 保：分数低于考生
    
    const diff = 30; // 梯度分差
    
    filtered.forEach(school => {
        const diffScore = school.最低分 - score;
        if (diffScore > diff) {
            if (chong.length < 4) chong.push(school);
        } else if (diffScore >= -diff && diffScore <= diff) {
            if (wen.length < 6) wen.push(school);
        } else {
            if (bao.length < 4) bao.push(school);
        }
    });
    
    return { schools: {冲: chong, 稳: wen, 保: bao}, isOutside: false };
}

// 匹配专业就业信息
function getEmploymentInfo(major) {
    if (!major) return null;
    
    // 模糊匹配
    major = major.toLowerCase();
    
    for (const [key, info] of Object.entries(employmentData)) {
        if (major.includes(key.toLowerCase()) || key.toLowerCase().includes(major)) {
            return { key, ...info };
        }
    }
    
    return null;
}

